#include <stdio.h>

int main() {
    for(int i=0; i<10; i++) // Estructura de control repetitiva: Para
    {
        printf("\t%i%f%s%d\n", i);
    }
    system("pause");
    return 0;
}

//Estrcuturas de control
/**
 * Estructuras de control selectivas: bifurcan acciones y el flujo que sigue el algoritmo
 * Estrcturas repetitivas: nos permiten repetir acciones determinadas veces segun la condicion propuesta
 * 
 * Pseudocodigo:
 * Selectivas:             Repetitivas:
 * Si..                    Para...
 * Sino                    Mientras...
 * Opcion                  hacer... mientras....
 * 
 * SINTAXIS DE ASIGNACION
 * = ES EQUIVALENTE A <- (flechitas)
 */